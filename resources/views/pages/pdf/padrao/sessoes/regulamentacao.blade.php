    <h4 style="padding-top: 0 !important;">Escopo dos Trabalhos</h4>

    <h5>a) Projeto</h5>
    <p>
        O pré-dimensionamento do sistema de geração fotovoltaica foi feito para a emissão dessa proposta. O
        dimensionamento final será feito com a aprovação da proposta. Nesse dimensionamento serão feitos todos as
        plantas, memoriais descritivos e todas as documentações exigidas para a homologação junto a concessionária
        (Coelba). Serão definidos os locais de instalação dos equipamentos e interligações do sistema fotovoltaico com o
        sistema elétrico local.
    </p>

    <h5>b) Instalação</h5>
    <p>
        Com a chegada dos equipamentos, será iniciada a instalação, na sequência abaixo:
    </p>
    <ul>
        <li>Estrutura de fixação dos painéis solares;</li>
        <li>Painéis solares e suas interligações elétricas;</li>
        <li>Inversores;</li>
        <li>Caixa de proteções e disjuntores;</li>
        <li>Interligação do sistema fotovoltaico com a instalação local.O pré-dimensionamento do sistema de geração
            fotovoltaica foi feito para a emissão dessa proposta.
        </li>
    </ul>

    <h5>c) Homologação</h5>
    <p>
        A homologação do sistema fotovoltaico, junto à concessionária - Coelba.
    </p>

    <h5>d) Acompanhamento</h5>

    <p>
        Acompanharemos todo o sistema durante os três primeiros meses após a interligação com a Coelba, onde serão
        feitas as seguintes rotinas:
    </p>
    <ul>
        <li>Verificação de temperatura dos contatos, das placas, inversores e painéis de proteção;</li>
        <li>Reaperto de contatos;</li>
        <li>Análise de desempenho do sistema;</li>
        <li>Limpeza das placas, caso necessário.</li>
    </ul>
    <h4>Prazos</h4>

    <ul>
        <li>1ª etapa - Projetos: 10 dias a partir da assinatura deste;</li>
        <li>2ª etapa - Entrada com as documentações na Coelba;</li>
        <li>3ª etapa - Materiais: até 15 dias após a entrega dos projetos;</li>
        <li>4ª etapa - Instalação: 4 dias da chegada dos materiais;</li>
        <li>5ª etapa - Solicitação de inspeção pela Coelba: 7 dias (Coelba);</li>
        <li>6ª etapa - Emissão do relatório de vistoria pela Coelba: 5 dias (Coelba);</li>
        <li>7ª etapa - Instalação do medidor bi-direcional: 7 dias (Coelba);</li>
        <li>8ª etapa - Testes e conclusão dos trabalhos.</li>
    </ul>

    <h4>Observações</h4>

    <ul>
        <li>Toda a montagem será realizada com pessoal treinado e habilitado, atendendo todas as legislações para
            serviço em altura e em eletricidade.
        </li>
        <li>Não incluso custos de adequação das instalações elétricas do cliente ao sistema fotovoltaico.</li>
        <li>O valor dessa proposta pode sofrer alteração, pois todos os equipamentos dependem da cotação do dolar.
        </li>
        <li>A análise financeira é meramente informativa, não devendo ser interpretada como garantia de resultados
            futuros. Não nos responsabilizamos pela utilização desses dados para tomada de decisão pelo cliente.
        </li>
    </ul>

